# convert variables in to new variables
# print new variables with correspondingly correct conversions

num1 = 99.23
num2 = 23
num3 = 150
string1 = "100"

num1_as_integer = int(num1)
print(num1_as_integer)

num2_as_float = float(num2)
print(num2_as_float)

num3_as_string = str(num3)
print(num3_as_string)

string1_as_integer = int(string1)
print(string1_as_integer)
